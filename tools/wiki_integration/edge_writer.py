if __name__ == "__main__":
    import tempfile
    print("edge_writer smoke test")
    print("=" * 60)
    with tempfile.NamedTemporaryFile(mode="w", suffix=".jsonl", delete=False) as f:
        # Pre-existing edge to test dedup
        f.write(json.dumps({
            "from": "voice/audio_001", "to": "concepts/foo.md",
            "type": "mentions", "created": "2026-05-09", "confidence": "high",
        }) + "\n")
        path = Path(f.name)

    edges = [
        # New
        make_voice_to_wiki_edge("audio_587", "concepts/founder-isolation.md",
                                 created="2026-05-10", score=0.92, bm25=24.6),
        # Duplicate (matches pre-existing)
        make_voice_to_wiki_edge("audio_001", "concepts/foo.md",
                                 created="2026-05-10", score=0.95),
        # Rejected predicate
        {"from": "x.md", "to": "y.md", "type": "rolled_back",
         "created": "2026-05-10", "confidence": "high"},
        # Tier C
        make_voice_to_new_edge("audio_588", "ideas/new-masterskaya.md",
                                created="2026-05-10"),
        make_neighbor_edge("ideas/new-masterskaya.md",
                            "ideas/tools-feedback-community-flywheel.md",
                            created="2026-05-10", bm25=8.55),
    ]
    result = append_edges(path, edges)
    print(f"Result: {result}")
    print()
    print("Final edges file:")
    for line in path.read_text().splitlines():
        print(f"  {line}")
    path.unlink()
    print("=" * 60)
